def string_to_number(s):
    x = 0
    sign = 1

    if s[0] == '-':
        s = s.replace(s[0], "")
        sign = -1
    fin = len(s) - 1
    for i in range(len(s)):
        x += int(s[i]) * (10 ** (fin - i))
    return x * sign

